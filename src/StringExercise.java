public class StringExercise {
    public static void main(String[] args) {
        String a = "We don't need no education \nWe don't need no thought control";
        System.out.println(a);

        String b = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(b);

        String c = "In windows, the main drive is usually C:\\";
        System.out.println(c);

        String d = "I can do backslashes \\, double backslashes \\\\, and the amazing triple backslashes \\\\\\!";
        System.out.println(d);
    }
}
